package br.com.carv.mscreditassessor.model;

public class ClientData {

    private Long id;
    private String name;

    private Integer age;

    public ClientData() {

    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public Integer getAge() {
        return getAge();
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }


}
